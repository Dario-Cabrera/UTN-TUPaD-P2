package ejercicio7.instruccionesyexpresiones;

public class Ejercicio7InstruccionesYExpresiones {
    public static void main(String[] args) {
        int x = 10; // Línea 1 Instrucción
        x = x + 5; // Línea 2 Expresión dentro de una instrucción
        System.out.println(x); // Línea 3 Instrucción
    }
    
}

// Una expresión es x + 5, porque devuelve un valor.
// Una instrucción es x = x + 5;, porque realiza una acción usando esa expresión.
