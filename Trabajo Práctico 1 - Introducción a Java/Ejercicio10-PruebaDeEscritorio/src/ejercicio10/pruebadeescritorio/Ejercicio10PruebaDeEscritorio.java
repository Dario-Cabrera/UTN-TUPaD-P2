package ejercicio10.pruebadeescritorio;

public class Ejercicio10PruebaDeEscritorio {
    public static void main(String[] args) {
        int a = 5; 
        int b = 2; 
        int resultado = a / b; 
        System.out.println("Resultado: " + resultado);
    }
    
}

// Este código realiza una división entera entre a y b. Cuando se divide un int por otro int, el resultado también es un int, y se descarta la parte decimal.

// El valor de resultado es 2, porque se realiza una división entera entre dos variables de tipo int.